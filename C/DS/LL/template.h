#include <stdio.h>
#include <stdlib.h>
#include <string.h>

struct node
{
	int d;
	char c;	
	struct node *next;
	struct node *prev;
};

typedef struct node* Node;

struct list
{
	Node head;
	Node tail;
	int size;
};

typedef struct list* List;

struct bstnode
{
	struct node* left;
	struct node* right;
	struct node* parent;	
	int d;	
	char c;
};

typedef struct bstnode* BstNode;


